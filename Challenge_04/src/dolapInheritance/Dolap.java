package dolapInheritance;

public class Dolap {
	private int en;
	private int boy;
	private int yukseklik;
	private int bolmeSayisi;
	private int kapakSayisi;
	
	public int getEn() {
		return en;
	}
	public void setEn(int en) {
		this.en = en;
	}
	public int getBoy() {
		return boy;
	}
	public void setBoy(int boy) {
		this.boy = boy;
	}
	public int getYukseklik() {
		return yukseklik;
	}
	public void setYukseklik(int yukseklik) {
		this.yukseklik = yukseklik;
	}
	public int getBolmeSayisi() {
		return bolmeSayisi;
	}
	public void setBolmeSayisi(int bolmeSayisi) {
		this.bolmeSayisi = bolmeSayisi;
	}
	public int getKapakSayisi() {
		return kapakSayisi;
	}
	public void setKapakSayisi(int kapakSayisi) {
		this.kapakSayisi = kapakSayisi;
	}
	
	
}
